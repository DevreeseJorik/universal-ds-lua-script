function ScriptData:new (o)
    o = o or {}
    setmetatable(o, self)
    self.__index = self
    return o
end


function ScriptData:update()
	self.nextScriptCommandAddr = Memory.read_u32_le(MemoryState.base + 0x29500)
	self.scriptDataPointer = Memory.read_u32_le(MemoryState.base +  0x3E950)
    self.scriptDataAddr = Memory.read_u32_le(self.scriptDataPointer)
    self.jumpTableAddr = Memory.read_u32_le(self.scriptDataAddr + 0x20)
    self.eventDataPointer = Memory.read_u32_le(MemoryState.base + 0x2949C)
    self.eventDataAddr = Memory.read_u32_le(self.eventDataPointer)
    self.eventId = Memory.read_u16_le(self.eventDataAddr + 0x2A)
	self.executionDetected = self:detectExecution()
	
	self:getJumpTableIndex()
	self.jumpAddr = (self.jumpTableIndex-1)*4 + self.jumpTableAddr
	self.jumpAmount = Memory.read_u32_le(self.jumpAddr)
	self.startOfScriptAddr = self.jumpAddr + self.jumpAmount + 0x4
end 

function ScriptData:getJumpTableIndex()
	self.isSpecialEvent = false
	if self.eventId > 0x283D then self.jumpTableIndex = self.eventId - 0x283D return end  
	if self.eventId > 0x7CF then 
		self.jumpTableIndex = self.eventId - 0x7CF
		self.isSpecialEvent = true
		return
	end
	self.jumpTableIndex = self.eventId
	if self.jumpTableIndex == 0 then self.jumpTableIndex = 1 end
end  

function ScriptData:detectExecution()
	local scriptDataValue = Memory.read_u16_le(self.scriptDataAddr)
	return (scriptDataValue == 0x5544) or (scriptDataValue == 0x4652)
end 